/** Названия языков на самом языке (в списке выбора). */
const LANG_LABEL: Record<Lang, string> = {
  ru: 'Русский',
  en: 'English',
  de: 'Deutsch',
  kz: 'Қазақша',
  uk: 'Українська',
  by: 'Беларуская',
};

const ALL_LANGS: Lang[] = ['ru', 'en', 'de', 'kz', 'uk', 'by'];

const LOCALE_TO_LANG: Record<string, Lang> = {
  ru: 'ru',
  en: 'en',
  de: 'de',
  kk: 'kz',
  kz: 'kz',
  uk: 'uk',
  be: 'by',
};

function mapLocaleToLang(tag: string): Lang | null {
  const primary = tag.trim().toLowerCase().split(/[-_]/)[0];
  return LOCALE_TO_LANG[primary] ?? null;
}

function detectDeviceLang(): Lang {
  if (typeof navigator === 'undefined') {
    return 'en';
  }
  const candidates = [...(navigator.languages ?? []), navigator.language].filter(Boolean);
  for (const tag of candidates) {
    const lang = mapLocaleToLang(tag);
    if (lang) {
      return lang;
    }
  }
  return 'en';
}

function syncDocumentLang(lang: Lang): void {
  if (typeof document === 'undefined') {
    return;
  }
  const map: Record<Lang, string> = {
    ru: 'ru',
    en: 'en',
    de: 'de',
    kz: 'kk',
    uk: 'uk',
    by: 'be',
  };
  document.documentElement.lang = map[lang];
}

function readStoredLang(): Lang {
  if (typeof localStorage !== 'undefined') {
    const v = localStorage.getItem(STORAGE_KEY);
    if (v && (ALL_LANGS as string[]).includes(v)) {
      return v as Lang;
    }
  }
  return detectDeviceLang();
}

@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly _lang = signal<Lang>(readStoredLang());

  constructor() {
    syncDocumentLang(this._lang());
  }

  /** Текущий код языка. */
  readonly lang = this._lang.asReadonly();

  /** Заголовки вкладки браузера (document.title). */
  readonly pageTitles = computed(() => PAGE_TITLE[this._lang()]);

  /** Строки для навбара (и нижней панели) на текущем языке. */
  readonly nav = computed(() => NAV[this._lang()]);

  /** Строки экрана «Ученики». */
  readonly studentsUi = computed(() => STUDENTS[this._lang()]);

  /** Строки экрана «Аккаунт». */
  readonly accountUi = computed(() => ACCOUNT[this._lang()]);

  /** Вход и регистрация. */
  readonly authUi = computed((): AuthStrings => AUTH[this._lang()]);

  /** Общие подписи юридических страниц. */
  readonly legalUi = computed(() => LEGAL_COMMON[this._lang()]);

  /** Политика обработки данных. */
  readonly legalDataUi = computed(() => LEGAL_DATA[this._lang()]);

  /** Политика cookies. */
  readonly legalCookiesUi = computed(() => LEGAL_COOKIES[this._lang()]);

  /** Календарь / расписание. */
  readonly calendarUi = computed(() => CALENDAR[this._lang()]);

  /** Общие подписи (селекты, пустые состояния). */
  readonly sharedUi = computed(() => SHARED[this._lang()]);

  /** Финансы. */
  readonly financeUi = computed(() => FINANCE[this._lang()]);

  /** Строки журнала изменений. */
  readonly activityLogUi = computed(() => ACTIVITY_LOG[this._lang()]);

  /** Главная. */
  readonly homeUi = computed(() => HOME[this._lang()]);

  /** Тарифы / подписка. */
  readonly pricingUi = computed(() => PRICING[this._lang()]);

  /** Панель super_admin. */
  readonly adminUi = computed(() => ADMIN[this._lang()]);

  readonly allLangs = ALL_LANGS;

  /** BCP 47 для Intl (даты, числа). */
  localeId(): string {
    const map: Record<Lang, string> = {
      ru: 'ru-RU',
      en: 'en-US',
      de: 'de-DE',
      kz: 'kk-KZ',
      uk: 'uk-UA',
      by: 'be-BY',
    };
    return map[this._lang()];
  }

  setLang(lang: Lang): void {
    this._lang.set(lang);
    syncDocumentLang(lang);
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem(STORAGE_KEY, lang);
    }
  }

  labelForLang(code: Lang): string {
    return LANG_LABEL[code];
  }

  /** Код валюты для отображения (USD, EUR, BYN …). */
  currencyLabel(code: RateCurrency): string {
    return code;
  }

  /** Подпись налогового режима. */
  taxModeLabel(mode: TaxMode | string): string {
    const labels = TAX_MODE_LABELS[this._lang()];
    return labels[mode as TaxMode] ?? String(mode);
  }

  /** Короткие подписи дней недели (пн–вс). */
  weekdayShortLabels(): string[] {
    const t = this.calendarUi();
    return [
      t.weekdayMon,
      t.weekdayTue,
      t.weekdayWed,
      t.weekdayThu,
      t.weekdayFri,
      t.weekdaySat,
      t.weekdaySun,
    ];
  }
}
